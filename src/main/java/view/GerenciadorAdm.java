    private void adicionarAdmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarAdmButtonActionPerformed
        String nomeAdm = txtNomeAdm.getText();
        String idadeAdm = txtIdadeAdm.getText();
        String enderecoAdm = txtEnderecoAdm.getText();
        String cpfAdm = txtFCpfAdm.getText();
        String senhaAdm = txtSenhaAdm.getText();
        
        if (nomeAdm == null || nomeAdm.length() == 0 
                || enderecoAdm.length() == 0 || enderecoAdm == null
                || idadeAdm.length() == 0 || idadeAdm == null
                || cpfAdm.length() == 0 || cpfAdm == null
                || senhaAdm.length() == 0 || senhaAdm == null){
            JOptionPane.showMessageDialog(null, "Por favor, preencha os campos corretamente");                 
        }
        else{       
            Administradores adms = new Administradores();
            DAOAdm daoadm = new DAOAdm();
            
            adms.setNome(txtNomeAdm.getText());
            adms.setSenha(txtSenhaAdm.getText());
            adms.setIdade(Integer.parseInt(txtIdadeAdm.getText()));
            adms.setEndereco(txtEnderecoAdm.getText());
            adms.setCpf(txtFCpfAdm.getText());
            
            daoadm.inserirAdministradores(adms);
        
            txtIdAdm.setText("");
            txtNomeAdm.setText("");
            txtSenhaAdm.setText("");
            txtIdadeAdm.setText("");
            txtEnderecoAdm.setText("");
            txtFCpfAdm.setText("");
            
            readTable();
        
        }  
    }//GEN-LAST:event_adicionarAdmButtonActionPerformed

    private void removerAdmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerAdmButtonActionPerformed
        if (jTAdm.getSelectedRow() != -1 ){
            
        int escolha = JOptionPane.showConfirmDialog(null, "Deseja Remover?", "Remover?", JOptionPane.YES_NO_OPTION);
        switch (escolha){
            default :
                JOptionPane.showMessageDialog(null, "O Administrador nao sera excluido.");
                break;
            case JOptionPane.YES_OPTION :
        try{
            Administradores adms = new Administradores();
            DAOAdm daoadm = new DAOAdm();
            
            adms.setId((int)jTAdm.getValueAt(jTAdm.getSelectedRow(), 0));
            
            daoadm.excluirAdm(adms);
            
            JOptionPane.showMessageDialog(null, "Administrador removido!");
            
            txtIdAdm.setText("");
            txtNomeAdm.setText("");
            txtSenhaAdm.setText("");
            txtIdadeAdm.setText("");
            txtEnderecoAdm.setText("");
            txtFCpfAdm.setText("");            
            
            readTable();
            
        } 
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistema indisponivel, tente novamente mais tarde.");
                }
            }
        } 
        else {
            JOptionPane.showMessageDialog(null, "Selecione um Administrador para remover");
        }
    }//GEN-LAST:event_removerAdmButtonActionPerformed

