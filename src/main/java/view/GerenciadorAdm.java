    public void readTable (){
        
        
        DefaultTableModel modelo = (DefaultTableModel) jTAdm.getModel();
        modelo.setNumRows(0);
        DAOAdm daoadm = new DAOAdm();
        
        for (Administradores adm: daoadm.listar()){
            
            modelo.addRow(new Object[]{
                    adm.getId(),
                    adm.getNome(),
                    adm.getIdade(),
                    adm.getEndereco(),
                    adm.getCpf(),
                    adm.getSenha()
            });
            
        }
    }
   

