    public void readTableFila (){
        
        
        DefaultTableModel modelo = (DefaultTableModel) jTFila.getModel();
        modelo.setNumRows(0);
        DAOPct daopct = new DAOPct();
        
                
        for (Pacientes pct: daopct.exibirFila()){
            
            try {                    
                modelo.addRow(new Object[]{
                    pct.getId(),
                    pct.getNome(),
                    pct.getIdade(),
                    pct.isProfissao(),
                    Utils.converter(pct.getDataa()),
                    pct.getVacinacao(),
                    pct.getPrioridade(),                   
                    
                });
            } catch (Exception ex) {
                Logger.getLogger(GerarRelatorio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
