    private void confirmarButtonFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonFilaActionPerformed
        if (jTFila.getSelectedRow() != -1){
            if (VacinaCheck.isSelected()){
                Pacientes pct = new Pacientes();
                DAOPct daopct = new DAOPct();

                pct.setId((int)jTFila.getValueAt(jTFila.getSelectedRow(), 0));

                try {              
                    pct.setDataa(Utils.converter(txtFDataFila.getText()));
                } catch (Exception ex) {
                    Logger.getLogger(filaDeVacinacao.class.getName()).log(Level.SEVERE, null, ex);
                }            
                if(VacinaCheck.isSelected()){
                    pct.vacinacao = "Sim";
                    JOptionPane.showMessageDialog(null, "Vacinado!");
                }
                else{
                    pct.vacinacao = " ";
                }

                daopct.adicionarVacina(pct);
                readTableFilaNaoVacinados();
            }
            else {
                JOptionPane.showMessageDialog(null, "Confirme a vacina!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um Paciente para ser vacinado!");
        }
    }//GEN-LAST:event_confirmarButtonFilaActionPerformed

