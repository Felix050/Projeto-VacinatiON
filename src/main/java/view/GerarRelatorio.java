    public void readTable (){
        
        
        DefaultTableModel modelo = (DefaultTableModel) jTRel.getModel();
        modelo.setNumRows(0);
        DAOPct daopct = new DAOPct();
        
        List <Pacientes> pacientes = daopct.listar();
        Collections.sort(pacientes);
                
        for (Pacientes pct: pacientes){
            
            try {                    
                modelo.addRow(new Object[]{
                    pct.getIdade(),
                    Utils.converter(pct.getDataa())
                });
            } catch (Exception ex) {
                Logger.getLogger(GerarRelatorio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

