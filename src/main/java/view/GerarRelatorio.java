    private void buscarButtonRelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonRelActionPerformed
            boolean maior90 = jRadioButton1.isSelected();
            boolean maior70 = jRadioButton2.isSelected();
            boolean maior50 = jRadioButton3.isSelected();
            boolean menor50 = jRadioButton4.isSelected();
            
            if (maior90 == false && maior70 == false && maior50 == false && menor50 == false){
                JOptionPane.showMessageDialog(null, "Selecione as idades!");
            }                
            else{
                try{
                    
                if(jRadioButton1.isSelected()){
                        readTableForRelatorioMaior90(txtFDataini.getText(), txtFDatafin.getText());
                }
                else if(jRadioButton2.isSelected()){
                        readTableForRelatorioMaior70(txtFDataini.getText(), txtFDatafin.getText());
                }
                else if(jRadioButton3.isSelected()){
                        readTableForRelatorioMaior50(txtFDataini.getText(), txtFDatafin.getText());
                }
                else if(jRadioButton4.isSelected()){
                        readTableForRelatorioMenor50(txtFDataini.getText(), txtFDatafin.getText());
                }
                    
                    }
            catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistema indisponivel, tente novamente mais tarde");
            }
        }       
    }//GEN-LAST:event_buscarButtonRelActionPerformed

